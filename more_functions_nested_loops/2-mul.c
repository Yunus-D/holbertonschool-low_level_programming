#include "main.h"

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Description:
 *	Cette fonction calcule simplement le produit de 'a' et 'b'.
 *	Aucune fonction de la bibliothèque standard n'est utilisée.
 *
 * Constraints:
 *	- Respect du style Betty
 *	- Pas de variable globale
 *	- Prototype: int mul(int a, int b);
 *
 * Return: The product of a and b
 */
int	mul(int a, int b)
{
	return (a * b);
}
